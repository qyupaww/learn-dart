void main(List<String> args) {
  var dayOfWeek = 2;

  if (dayOfWeek == 1) {
    print("Hari Senin");
  } else if (dayOfWeek == 2) {
    print("Hari Selasa");
  } else if (dayOfWeek == 3) {
    print("Hari Rabu");
  } else if (dayOfWeek == 4) {
    print("Hari Kamis");
  } else if (dayOfWeek == 5) {
    print("Hari Jumat");
  } else if (dayOfWeek == 6) {
    print("Hari Sabtu");
  } else if (dayOfWeek == 7) {
    print("Hari Minggu");
  } else {
    print("angka invalid");
  }
}
